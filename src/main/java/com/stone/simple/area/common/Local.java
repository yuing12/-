package com.stone.simple.area.common;

public class Local {
	
	int lno;
	String lname;
	City cityNo;
	String x;
	String y;
	
	public int getLno() {
		return lno;
	}
	public void setLno(int lno) {
		this.lno = lno;
	}
	public String getLname() {
		return lname;
	}
	public void setLname(String lname) {
		this.lname = lname;
	}
	public City getCityNo() {
		return cityNo;
	}
	public void setCityNo(City cityNo) {
		this.cityNo = cityNo;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	
	

}
